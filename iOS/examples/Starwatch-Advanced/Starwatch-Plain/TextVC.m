#import "TextVC.h"
#import "Utilities.h"
#import "SWCUtility.h"


@implementation TextVC

@synthesize textView;



-(id)init {
    NSString * nibname = [NSString stringWithFormat:@"TextVC_%@",
                          [Utilities getNibSuffix]];
    
    self = [super initWithNibName:nibname
                           bundle:nil];
    
    
    
    if (self) {
        [self initializeSWVC:@"TextVC"
               new_global_id:@""
    new_respond_to_callbacks:YES];
    }
    
    
    return self;
}

-(void)viewDidLoad {
    // we're tracking this as a webview now so we can see when they scroll to the bottom.
    [textView loadHTMLString:[TextVC sampleText] baseURL:nil];
    
    textView.scrollView.delegate = self;
    
}


-(void)scrollViewDidScroll:(UIScrollView *)scrollView {
    
    float max_height = scrollView.contentSize.height - scrollView.bounds.size.height;
    float active_height = scrollView.contentOffset.y;
    
    
    if (max_height == active_height) {
        [self logScrolledToBottom];
    }
    else if (scrollView.contentOffset.y == 0) {
        [self logScrolledToTop];
    }
    
}


-(void)viewDidUnload {
    
    [self setTextView:nil];
    
    [super viewDidUnload];
}

